import math

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi))
print(math.ceil(pi)) # round number up
print(math.floor(pi)) # round number down
print(abs(-pi)) # give absolute value
print(pow(pi,2)) # pi to the power of 2
print(math.sqrt(pi)) # give square root
print(-pi) # you can print negative number in pi
print(max(x,y,z)) # print largest value
print(min(x,y,z)) # print smallest value