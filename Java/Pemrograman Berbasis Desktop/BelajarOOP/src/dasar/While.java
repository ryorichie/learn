public class While {

    public static void main(String[] args) {
        int i = 1;

        // while
        while (i <= 20) {
            System.out.println(i);
            i++;
        }

        int y = 1;
        // do - while
        do {
            System.out.println(y);
            y++;
        } while (y <= 10);
    }
}
