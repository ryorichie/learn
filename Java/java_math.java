public class java_math {
    public static void main(String[] args) {
        System.out.println(Math.max(17, 27)); //Math.max
        System.out.println(Math.min(17212, 12217)); //Math.min
        System.out.println(Math.sqrt(1211)); // Math.sqrt
        System.out.println(Math.abs(-1)); // Math.abs
        System.out.println(Math.random() * 101);  // Math.random
    }    
}
