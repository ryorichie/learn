public class java_if_else {
    public static void main(String[] args) {
        int x = 20;
        int y = 20;

        if ( x > y) {
            System.out.println("x is greater than y");
        } else if (x < y) {
            System.out.println("y is greater than x");
        } else {
            System.out.println("x is equal to y");
        }
    }
}
