name = "ryoricHie"

print(len(name)) # Return length of variable
print(name.find("ie")) # Return location of character
print(name.capitalize()) # Capitalize first char, then the rest is lower
print(name.lower()) # lowercase all char
print(name.upper()) # uppercase all char
print(name.isdigit()) # Check if the sting is digit or not
print(name.isalpha()) # Check if the string is alpha or not, isalpha(not contain space and number)
print(name.count("H")) # Count character in string, Strict Cap
print(name.replace("r", "k")) # Replace character
print(name*10) # You can print variable many times